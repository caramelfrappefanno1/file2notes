@app.route("/notes")
def notes():
    try:
        # FILE MODE
        if "file" in request.files:
            file = request.files["file"]
            filename = secure_filename(file.filename)
            path = os.path.join(UPLOAD_FOLDER, filename)
            file.save(path)
            ext = filename.split(".")[-1].lower()

            if ext == "pdf":
                text = extract_text_from_pdf(path)
            elif ext == "docx":
                text = extract_text_from_docx(path)
            elif ext == "txt":
                with open(path, "r", encoding="utf8") as f:
                    text = f.read()
            else:
                return jsonify({"error": "Unsupported file"})

            prompt = f"Summarize the given text in bullet point form. Notes must be clear, short, and concise. {text}"

            completion = client.chat.completions.create(
            model="gpt-4o-mini",
            messages=[{"role": "user", "content": prompt}],
            max_tokens=4000
            )

            return completion

        # LINK MODE
        data = request.json

        if data.get("link"):
            text = extract_text_from_url(data["link"])
            if not text:
                return jsonify({"error": "Failed to extract link"})

            prompt = f"Summarize the given text in bullet point form. Notes must be clear, short, and concise. {text}"

            completion = client.chat.completions.create(
            model="gpt-4o-mini",
            messages=[{"role": "user", "content": prompt}],
            max_tokens=4000
            )

            return completion

        return jsonify({"error": "Invalid request"})


    except Exception as e:
        return jsonify({"error": str(e)})
    
@app.route("/delete_quiz", methods=["POST"])
def delete_quiz():
    ts = request.json.get("timestamp")
    with open(HISTORY_FILE, "r") as f:
        history = json.load(f)
    
    # Keep everything EXCEPT the one we want to delete
    history = [q for q in history if q.get("timestamp") != ts]
    
    with open(HISTORY_FILE, "w") as f:
        json.dump(history, f, indent=4)
    return jsonify({"status": "success"})

@app.route("/rename_quiz", methods=["POST"])
def rename_quiz():
    data = request.json
    ts = data.get("timestamp")
    new_name = data.get("new_name")
    
    with open(HISTORY_FILE, "r") as f:
        history = json.load(f)
    
    for q in history:
        if q.get("timestamp") == ts:
            q["custom_name"] = new_name
            break
            
    with open(HISTORY_FILE, "w") as f:
        json.dump(history, f, indent=4)
    return jsonify({"status": "success"})

