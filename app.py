@app.route("/quizgen", methods=["POST"])
def genquiz():
    text = ""
    
    # Check if a file was uploaded
    if 'file' in request.files:
        file = request.files['file']
        if file.filename != '':
            filename = secure_filename(file.filename)
            filepath = os.path.join(UPLOAD_FOLDER, filename)
            file.save(filepath)

            # Use your extraction functions from util.py
            if filename.endswith(".pdf"):
                text = extract_text_from_pdf(filepath)
            elif filename.endswith(".docx"):
                text = extract_text_from_docx(filepath)
            else:
                with open(filepath, 'r', encoding='utf-8') as f:
                    text = f.read()
    
    # Otherwise, check for a link in the form data
    else:
        link = request.form.get("link", "").strip()
        if link:
            text = extract_text_from_url(link)

    if not text:
        return jsonify({"error": "No text content found"}), 400

    # Generate the quiz (Replacing the undefined 'questions_list' variable)
    quiz_data = create_quiz(text)
    
    if "error" in quiz_data:
        return jsonify(quiz_data), 500

    save_quiz(quiz_data)
    return jsonify(quiz_data)

